#include<iostream>
using namespace std;

int main(){
    float a, b;
    cout <<"PROGRAM ARITMATIKA"<<endl;
    cout <<"Masukkan Nilai A : ";
    cin>>a;

    cout <<"Masukkan Nilai B : ";
    cin>> b;

    cout << "Nilai A : "<< a <<endl;
    cout << "Nilai B : "<< b <<endl;

    cout << a <<" + "<< b <<" = "<< a+b <<endl;
    cout << a <<" - "<< b <<" = "<< a-b <<endl;
    cout << a <<" * "<< b <<" = "<< a*b <<endl;
    cout << a <<" / "<< b <<" = "<< a/b <<endl;
    return 0;
}
